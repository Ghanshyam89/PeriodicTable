    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed

  try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","");
            Statement stmt = con.createStatement();
            String str="select*from element where Z=1";
            ResultSet rs =stmt.executeQuery(str);
            while (rs.next()){
                int a = rs.getInt("Z");
                String b =rs.getString("ELEMENT_NAME");
                int c =rs.getInt("ATOMIC_WEIGHT");
                int d =rs.getInt("DENSITY");
                int e =rs.getInt("BOILLING_POINT");
                int f =rs.getInt("MELTING_POINT");
           this.setVisible(false);
               new properties().setVisible(true);
           t1.setText(Integer.toString(a));
           t2.setText(b);
           t3.setText(Integer.toString(c));
           t4.setText(Integer.toString(d));
           t5.setText(Integer.toString(e));
           t6.setText(Integer.toString(f));
           
        }
  }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
       
                           
    }//GEN-LAST:event_b1ActionPerformed

